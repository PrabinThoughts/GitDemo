
public abstract class ParentFlightAbstractionDemo {
	// An abstract class needs to be defined as abstract

		
		public void engine() 
		{
			System.out.println("This is engine guidelines");
		}
		
		public void safetyinstructions() 
		{
			System.out.println("These are safety guidelines");
		}
		
		public abstract void colour() ; 
		
		// Method without body in an abstract class needs to be defined as abstract
		// Access modifiers used inside abstract class can be anything apart from Private.
	}


